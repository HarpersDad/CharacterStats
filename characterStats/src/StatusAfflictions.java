public class StatusAfflictions
{
    Boolean poison;
    Boolean burn;
    Boolean blind;
    Boolean asleep;
    Boolean stun;
    Boolean berserk;

    StatusAfflictions(Boolean poison, Boolean burn, Boolean blind, Boolean asleep, Boolean stun, Boolean berserk)
    {
        this.poison = poison;
        this.burn = burn;
        this.blind = blind;
        this.asleep = asleep;
        this.stun = stun;
        this.berserk = berserk;
    }
}

public class StatusAfflictions
{
    boolean poison;
    boolean burn;
    boolean blind;
    boolean sleep;
    boolean stun;
    boolean berserk;

    StatusAfflictions(boolean poison, boolean burn, boolean blind, boolean sleep, boolean stun, boolean berserk)
    {
        this.poison = poison;
        this.burn = burn;
        this.blind = blind;
        this.sleep = sleep;
        this.stun = stun;
        this.berserk = berserk;
    }
}
